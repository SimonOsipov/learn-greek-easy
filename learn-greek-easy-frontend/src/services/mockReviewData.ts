/**
 * Helper to create initial spaced repetition data for new cards
 */
function createInitialSRData(cardId: string, deckId: string): SpacedRepetitionData {
  return {
    cardId,
    deckId,
    interval: 0,
    easeFactor: 2.5,
    repetitions: 0,
    state: 'new',
    step: 0,
    dueDate: new Date(), // New cards are due immediately
    lastReviewed: null,
    reviewCount: 0,
    successCount: 0,
    failureCount: 0,
    successRate: 0,
  };
}

